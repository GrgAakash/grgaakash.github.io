"""
Compare our computed TI_2 and chain positions for flagpole partitions with defc=10
against the HLLL appendix data (app10_11.pdf).

Key finding: HLLL chains operate within a FIXED grid size n (rdv length L),
while our partition-level NU changes grid size. So we verify:
1. TI_2(mu) is correctly placed in C_mu
2. The position within the chain is consistent with our formulas
3. All chain data (a, m, h) matches
"""


def parse_hlll_partition(s):
    """Parse HLLL compact notation like '(AA987654321)' into a partition tuple."""
    s = s.strip().replace('N', '').strip('() ')
    parts = []
    for ch in s:
        if ch == 'A':
            parts.append(10)
        elif ch == 'B':
            parts.append(11)
        else:
            parts.append(int(ch))
    parts = sorted(parts, reverse=True)
    return tuple(parts)


def partition_to_hlll(mu):
    """Convert partition tuple to HLLL compact notation."""
    parts = sorted(mu, reverse=True)
    s = ''
    for p in parts:
        if p == 10:
            s += 'A'
        elif p == 11:
            s += 'B'
        else:
            s += str(p)
    return f'({s})'


def mu_to_hlll_label(mu):
    """Convert mu to HLLL subscript label."""
    return partition_to_hlll(mu).replace('(', '').replace(')', '')



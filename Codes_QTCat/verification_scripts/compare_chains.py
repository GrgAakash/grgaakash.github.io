def main():
    print("=" * 100)
    print("FLAGPOLE PARTITIONS (defc=10): TI_2 placement in HLLL chains")
    print("=" * 100)

    # Enumerate flagpole partitions
    flagpole_params = []
    for lam_size in range(0, 11):
        for lam in partitions_of(lam_size):
            lam1 = max(lam) if lam else 0
            ell_lam = len(lam) if lam else 0
            for eps in [0, 1]:
                a_val = 9 - lam1 - ell_lam - lam_size
                if a_val < max(2, a0(lam)):
                    continue
                v = build_v(lam, a_val, eps)
                defc_val = compute_defc(v)
                if defc_val != 10:
                    continue
                ti2_partition = reduced_dv_to_partition(v)
                ti_rdv = compute_ti_from_params(lam, a_val, eps)
                mu = ti_rdv_to_original_partition(ti_rdv)
                flagpole_params.append({
                    'lambda': lam, 'a': a_val, 'eps': eps,
                    'v': v, 'ti2_part': ti2_partition, 'mu': mu,
                })

    all_ok = True

    for idx, fp in enumerate(flagpole_params):
        mu = fp['mu']
        lam = fp['lambda']
        a_val = fp['a']
        eps = fp['eps']
        ti2 = fp['ti2_part']
        v_ti2 = fp['v']

        print(f"\n{'─' * 100}")
        print(f"#{idx+1}: mu = {mu}  ->  C_({mu_to_hlll_label(mu)})")
        print(f"     lambda = {lam if lam else '()'},  a = {a_val},  eps = {eps}")
        print(f"     TI_2(mu) = {partition_to_hlll(ti2)}  (partition sum = {sum(ti2)})")
        print(f"     v(lam,a,eps) = {list(v_ti2)}  (L = {len(v_ti2)})")
        print(f"     area(TI_2) = {compute_area(v_ti2)},  dinv(TI_2) = {compute_dinv(v_ti2)}")

        if mu not in HLLL_DATA_K10:
            print(f"  !! mu = {mu} NOT in HLLL data")
            all_ok = False
            continue

        hlll = HLLL_DATA_K10[mu]
        chain_strs = hlll['chain']
        chain = [parse_hlll_partition(s) for s in chain_strs]
        a_vals = hlll['a']
        m_vals = hlll['m']
        h_vals = hlll['h']

        # Find TI_2 in the chain
        ti2_pos = None
        for i, elem in enumerate(chain):
            if elem == ti2:
                ti2_pos = i
                break

        if ti2_pos is None:
            print(f"  !! TI_2 NOT FOUND in HLLL chain!")
            print(f"     Chain: {[partition_to_hlll(e) for e in chain]}")
            all_ok = False
            continue

        print(f"  -> TI_2 found at position {ti2_pos} / {len(chain)-1} in C_({mu_to_hlll_label(mu)})")

        # Verify dinv at TI_2 position matches HLLL 'a' value
        our_dinv = compute_dinv(v_ti2)

        # Compute dinv for each chain element to find where our dinv matches
        print(f"\n     Full chain C_({mu_to_hlll_label(mu)}) with dinv values:")
        chain_dinvs = []
        for i, elem in enumerate(chain):
            rdv = partition_to_reduced_dv(elem)
            dinv_val = compute_dinv(rdv)
            area_val = compute_area(rdv)
            defc_val = compute_defc(rdv)
            L = len(rdv)
            is_N = 'N' in chain_strs[i]
            marker = ""
            if i == ti2_pos:
                marker = "  <== TI_2"
            if is_N:
                marker += "  [N]"
            chain_dinvs.append(dinv_val)
            print(f"       {i:3d}: {partition_to_hlll(elem):22s}  "
                  f"dinv={dinv_val:3d}  area={area_val:3d}  defc={defc_val:3d}  L={L:2d}{marker}")

        # Verify all defc = 10
        for i, elem in enumerate(chain):
            rdv = partition_to_reduced_dv(elem)
            if compute_defc(rdv) != 10:
                print(f"  !! Element {i} has defc = {compute_defc(rdv)} != 10")
                all_ok = False

    print(f"\n\n{'=' * 100}")
    print(f"All TI_2 found: {'YES' if all_ok else 'NO (see errors above)'}")
    print(f"{'=' * 100}")

    # Summary table
    print(f"\n{'=' * 100}")
    print("SUMMARY: Flagpole partitions and their position in HLLL chains")
    print(f"{'=' * 100}")
    print(f"{'#':>3s}  {'mu':>28s}  {'lambda':>12s}  {'a':>3s}  {'eps':>3s}  "
          f"{'TI_2':>22s}  {'pos':>5s}  {'chain_len':>9s}  {'dinv(TI2)':>9s}")
    print("-" * 110)
    for idx, fp in enumerate(flagpole_params):
        mu = fp['mu']
        ti2 = fp['ti2_part']
